/**
* @brief Function implementing the lvgl_task thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_lvgl_app */
void lvgl_app(void const * argument)
{
  /* USER CODE BEGIN lvgl_app */
  /* Infinite loop */
    FS_ListPath("/");
	lv_init();	
	lv_port_disp_init();
	lv_port_indev_init();
	
	lv_demo_benchmark();
	
	for(;;)
	{
		osMutexWait(lvgl_MutexHandle, portMAX_DELAY);	
		lv_task_handler();
		osMutexRelease(lvgl_MutexHandle);
		osDelay(3);
	}
  
  /* USER CODE END lvgl_app */
}

